package ejercicio0928;

public class Titulo {
	
	private int anyo;
	private String nombre;

	
	
	
	
	
	
	
	public Titulo(int anyo, String nombre) {
		this.anyo = anyo;
		this.nombre = nombre;
	}
	
	public int getAnyo() {
		return anyo;
	}
	
	public void setAnyo(int anyo) {
		this.anyo = anyo;
	}
	
	public String getNombre() {
		return nombre;
	}
	
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	

}
